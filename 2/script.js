function operazioni(){

    var a = parseInt(prompt("inserisci a"))
    var b = parseInt(prompt("inserisci b"))
    
    var somma = a+b
    var differenza = a-b
    var prodotto = a*b
    var quoziente = a/b
 
    alert(

        "somma: "      + somma      + "\n" + 
        "differenza: " + differenza + "\n" +
        "prosciutto: " + prodotto   + "\n" + 
        "quoziente: "  + quoziente  + "\n"
        
    )

}